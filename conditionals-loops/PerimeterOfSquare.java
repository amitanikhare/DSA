//Perimeter Of Square
import java.util.*;
class PerimeterOfSquare {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        System.out.println("Enter sides :- ");
        int a = in.nextInt();
        int peri = 4*a;
        System.out.println("Perimeter Of Square :- "+peri);
    }
}
